# Show how variables can change their values and even types.

counter = 0
print(f"Initial counter: {counter}, type: {type(counter)}")

counter += 5
print(f"+5 counter: {counter}, type: {type(counter)}")

data = 100
print(f"\ndata as integer: {data}, type: {type(data)}")

data = "hello"
print(f"\ndata as integer: {data}, type: {type(data)}")

data = 3.14
print(f"\ndata as integer: {data}, type: {type(data)}")

data = [100, 2, 6]
print(f"\ndata as integer: {data}, type: {type(data)}")